        public async Task<bool> SendContactEmailAsync(ContactRequest request)
        {
            try
            {
                _logger.LogInformation("üìß Starting email send process for: {Email}", request.Email);
                
                // Company email olu≈ütur
                var companyMessage = CreateContactMessage(request, isCompanyEmail: true);
                
                // Customer confirmation email olu≈ütur
                var customerMessage = CreateContactMessage(request, isCompanyEmail: false);

                using var client = new SmtpClient();
                
                // SSL sertifika doƒürulamasƒ±nƒ± ge√ßici olarak devre dƒ±≈üƒ± bƒ±rak (production i√ßin d√ºzenlenecek)
                client.ServerCertificateValidationCallback = (s, c, h, e) => true;
                
                var smtpHost = _configuration["Smtp:Host"];
                var smtpPort = int.Parse(_configuration["Smtp:Port"] ?? "587");
                var smtpUsername = _configuration["Smtp:Username"];
                
                _logger.LogInformation("üîó Connecting to SMTP: {Host}:{Port}", smtpHost, smtpPort);
                
                var secureOption = MailKit.Security.SecureSocketOptions.None;
                if (smtpPort == 465)
                {
                    secureOption = MailKit.Security.SecureSocketOptions.SslOnConnect;
                }
                else if (smtpPort == 587)
                {
                    secureOption = MailKit.Security.SecureSocketOptions.StartTls;
                }
                await client.ConnectAsync(smtpHost, smtpPort, secureOption);
                _logger.LogInformation("‚úÖ SMTP connection established");

                _logger.LogInformation("üîê Authenticating with username: {Username}", smtpUsername);
                await client.AuthenticateAsync(smtpUsername, _configuration["Smtp:Password"]);
                _logger.LogInformation("‚úÖ SMTP authentication successful");

                // Company email g√∂nder
                _logger.LogInformation("üì§ Sending company email...");
                await client.SendAsync(companyMessage);
                _logger.LogInformation("‚úÖ Company email sent for contact form: {Email}", request.Email);

                // Customer confirmation email g√∂nder
                _logger.LogInformation("üì§ Sending customer confirmation email...");
                await client.SendAsync(customerMessage);
                _logger.LogInformation("‚úÖ Customer confirmation email sent: {Email}", request.Email);

                await client.DisconnectAsync(true);
                _logger.LogInformation("üîå SMTP connection closed");
                return true;
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "‚ùå Failed to send contact email for: {Email}. Error: {Error}", request.Email, ex.Message);
                return false;
            }
        }

